    private void jPanel1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseEntered
        // TODO add your handling code here:
        try{
            conn=DBConnection.ConnectDB();
            String sql="SELECT * FROM students WHERE st_id='"+jLabel9.getText()+"'";
            Statement st=conn.createStatement();
            rs=st.executeQuery(sql);
            if(rs.next()){
                byte[] img=rs.getBytes("profile_pic");
                ImageIcon image=new ImageIcon(img);
                Image im=image.getImage();
                Image s=im.getScaledInstance(jLabel11.getWidth(), jLabel11.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon newImage=new ImageIcon(s);
                jLabel11.setIcon(newImage);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);}
        finally{
            try{
                conn.close();
                pst.close();
                rs.close();
            }
            catch(Exception e){
            }
        }
    }//GEN-LAST:event_jPanel1MouseEntered

    private void jPanel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel6MouseClicked
        // TODO add your handling code here:
        conn=DBConnection.ConnectDB();
        String sql="UPDATE students SET profile_pic=? WHERE st_id='"+jLabel9.getText()+"'";
        try{
            pst=conn.prepareStatement(sql);
            InputStream is= new FileInputStream(new File(s));
            pst.setBlob(1,is);
            int r=pst.executeUpdate();
            if(r>0){
                JOptionPane.showMessageDialog(rootPane,"Profile picture updated");
                String uname= jLabel18.getText();
                new Edit_st_details(uname).setVisible(true);
                new Edit_address1().setVisible(false);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Error generated while updating data..!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);}
        finally{
            try{
                conn.close();
                st.close();
                rs.close();

            }
            catch(Exception e){
            }
        }
    }//GEN-LAST:event_jPanel6MouseClicked

