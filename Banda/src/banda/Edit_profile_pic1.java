    private void jPanel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel7MouseClicked
        // TODO add your handling code here:
        JFileChooser f= new JFileChooser();
        f.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter fil= new FileNameExtensionFilter("jpg","png");
        f.addChoosableFileFilter(fil);
        int result=f.showSaveDialog(null);
        if(result==JFileChooser.APPROVE_OPTION){
            File selectedFile=f.getSelectedFile();
            String path=selectedFile.getAbsolutePath();
            jLabel6.setIcon(ResizeImage(path));
            s= path;
        }
    }//GEN-LAST:event_jPanel7MouseClicked

